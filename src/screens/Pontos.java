    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Position inicio;
        Position fim;
        if (!jX1.getText().isEmpty() && !jY1.getText().isEmpty() && !jX2.getText().isEmpty() && !jY2.getText().isEmpty()) {
            if (jX1.getText().matches("[0-9]*") && jY1.getText().matches("[0-9]*") && jX2.getText().matches("[0-9]*") && jY2.getText().matches("[0-9]*")) {
                inicio = new Position(Integer.parseInt(jX1.getText()), Integer.parseInt(jY1.getText()));
                fim = new Position(Integer.parseInt(jX2.getText()), Integer.parseInt(jY2.getText()));
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Coordenadas Incorretas");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Coordenadas Incorretas");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

